;COUNT THE NUMBER OF CHARACTERS IN A LINE
.MODEL SMALL
.DATA
COUNT DB 0
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV CX,0
    L:
    MOV AH,1H
    INT 21H
    CMP AL,0DH
    JMP EXIT
    INC COUNT
    LOOP L
    EXIT:
    MOV AH, 4CH
    INT 21H
    
    MAIN ENDP
END MAIN